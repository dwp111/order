package cn.tootoo.soa.oms.utils;

/**
 * Description:<br>
 * 
 * @author nekohy
 * @version 1.0
 */
public class GoodsCountBean {

	private Long goodsId;

	private Long count;

	public Long getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(Long goodsId) {
		this.goodsId = goodsId;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

	public GoodsCountBean() {
	}

	@Override
	public String toString() {
		return "GoodsCountBean [goodsId=" + goodsId + ", count=" + count + "]";
	}

	public GoodsCountBean(Long goodsId, Long count) {
		super();
		this.goodsId = goodsId;
		this.count = count;
	}

}
